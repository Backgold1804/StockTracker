package com.example.stocktracker.Home;

public class Data {

    String home_stock_name;
    int home_amount_price;
    int home_holdings;
    int home_user_price;
    float home_profit;

    public String getHome_stock_name() {
        return home_stock_name;
    }

    public void setHome_stock_name(String home_stock_name) {
        this.home_stock_name = home_stock_name;
    }

    public int getHome_amount_price() {
        return home_amount_price;
    }

    public void setHome_amount_price(int home_amount_price) {
        this.home_amount_price = home_amount_price;
    }

    public int getHome_holdings() {
        return home_holdings;
    }

    public void setHome_holdings(int home_holdings) {
        this.home_holdings = home_holdings;
    }

    public int getHome_user_price() {
        return home_user_price;
    }

    public void setHome_user_price(int home_user_price) {
        this.home_user_price = home_user_price;
    }

    public float getHome_profit() {
        return home_profit;
    }

    public void setHome_profit(float home_profit) {
        this.home_profit = home_profit;
    }
}
